package com.prj.biz.enums;

/** 
* @Description: 渠道服务协议类型
* @date 2016年4月19日 
* @author dfsoft
*/
public enum SAstatusEnum {

	SASTATUS_SUCCESS("1","待支付"),
	SASTATUS_UNPAID("2","待支付"),
	SASTATUS_REVOKE("3","作废");
	
	private String code;
	private String desc;
	
	SAstatusEnum(String code,String desc){
		this.code = code;
		this.desc = desc;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
