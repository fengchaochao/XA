package com.prj.biz.bean.businessclassification;

import com.prj.biz.bean._base.BaseEntity;

/** 
 * 描述: 商家分类及抽成信息 实体类<br>
 * @author Liang
 * @date 2017-07-11
 * 
 */
public class BusinessClassification extends BaseEntity  
{

    private static final long	serialVersionUID	= 1L;

    //  
    private String id;
    // 分类名称 
    private String categoryName;
    // 第一次锁定抽成 
    private String firstLockInto;
    // 锁定消费者消费抽成 
    private String consumption;
    // 引流消费抽成 
    private String drainageConsumption;
    // 编号 
    private String classificationNumber;
    // 创建时间 
    private String createDate;
    // 修改时间 
    private String modifyDate;


    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getCategoryName() {
        return categoryName;
    }
    public void setCategoryName(String categoryName) {
        this.categoryName = categoryName;
    }


    public String getFirstLockInto() {
        return firstLockInto;
    }
    public void setFirstLockInto(String firstLockInto) {
        this.firstLockInto = firstLockInto;
    }


    public String getConsumption() {
        return consumption;
    }
    public void setConsumption(String consumption) {
        this.consumption = consumption;
    }


    public String getDrainageConsumption() {
        return drainageConsumption;
    }
    public void setDrainageConsumption(String drainageConsumption) {
        this.drainageConsumption = drainageConsumption;
    }


    public String getClassificationNumber() {
        return classificationNumber;
    }
    public void setClassificationNumber(String classificationNumber) {
        this.classificationNumber = classificationNumber;
    }


    public String getCreateDate() {
        return createDate;
    }
    public void setCreateDate(String createDate) {
        this.createDate = createDate;
    }


    public String getModifyDate() {
        return modifyDate;
    }
    public void setModifyDate(String modifyDate) {
        this.modifyDate = modifyDate;
    }

}
