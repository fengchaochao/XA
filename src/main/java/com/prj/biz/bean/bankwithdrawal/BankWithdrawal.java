package com.prj.biz.bean.bankwithdrawal;

import com.prj.biz.bean._base.BaseEntity;

/** 
 * 描述: 提现账户表 实体类<br>
 * @author Liang
 * @date 2017-08-07
 * 
 */
public class BankWithdrawal extends BaseEntity  
{

    private static final long	serialVersionUID	= 1L;

    //  
    private String id;
    // 待提现金额 
    private String totalRevenue;
    // 卡号 
    private String brankNumber;
    // 持卡人姓名 
    private String brankName;
    // 银行预留的联系方式 
    private String brankPhone;
    //身份证号
    private String idCard;
    // 银行类型 
    private String brankAddress;
    // 用户ID 
    private String userId;
    //账户类型1-微信用户 2-支付宝用户  3-银行卡
    private String accountType;

	public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }


    public String getTotalRevenue() {
        return totalRevenue;
    }
    public void setTotalRevenue(String totalRevenue) {
        this.totalRevenue = totalRevenue;
    }


    public String getBrankNumber() {
        return brankNumber;
    }
    public void setBrankNumber(String brankNumber) {
        this.brankNumber = brankNumber;
    }


    public String getBrankName() {
        return brankName;
    }
    public void setBrankName(String brankName) {
        this.brankName = brankName;
    }


    public String getBrankPhone() {
        return brankPhone;
    }
    public void setBrankPhone(String brankPhone) {
        this.brankPhone = brankPhone;
    }


    public String getBrankAddress() {
        return brankAddress;
    }
    public void setBrankAddress(String brankAddress) {
        this.brankAddress = brankAddress;
    }


    public String getUserId() {
        return userId;
    }
    public void setUserId(String userId) {
        this.userId = userId;
    }
	public String getIdCard() {
		return idCard;
	}
	public void setIdCard(String idCard) {
		this.idCard = idCard;
	}
	public String getAccountType() {
		return accountType;
	}
	public void setAccountType(String accountType) {
		this.accountType = accountType;
	}

}
