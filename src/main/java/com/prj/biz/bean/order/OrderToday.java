package com.prj.biz.bean.order;

import java.util.List;

import com.prj.biz.bean._base.BaseEntity;

/** 
 * 描述: 订单表Api返利显示及营业统计 实体类<br>
 * @author Liang
 * @date 2017-08-01
 * 
 */
public class OrderToday extends BaseEntity  
{

    private static final long	serialVersionUID	= 1L;
    //日期/月份
    private String date;
    //返利详细列表
    private List<Order> orderList;
    
    //月总返利数
    private String bonusNum;
    
    //销售额
    private String salesNum;
    //销售量
    private String goodsNum;
    //订单量
    private String orderNum;
    //实收额
    private String paidAmount;
    
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public List<Order> getOrderList() {
		return orderList;
	}
	public void setOrderList(List<Order> orderList) {
		this.orderList = orderList;
	}
	public String getBonusNum() {
		return bonusNum;
	}
	public void setBonusNum(String bonusNum) {
		this.bonusNum = bonusNum;
	}
	public String getSalesNum() {
		return salesNum;
	}
	public void setSalesNum(String salesNum) {
		this.salesNum = salesNum;
	}
	public String getGoodsNum() {
		return goodsNum;
	}
	public void setGoodsNum(String goodsNum) {
		this.goodsNum = goodsNum;
	}
	public String getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}
	public String getPaidAmount() {
		return paidAmount;
	}
	public void setPaidAmount(String paidAmount) {
		this.paidAmount = paidAmount;
	}
    

}
